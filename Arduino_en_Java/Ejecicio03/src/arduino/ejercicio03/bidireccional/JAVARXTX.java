    //Cuando se recibe un dato por el puerto serial.
    private static final SerialPortEventListener listener = new SerialPortEventListener(){
        @Override
        public void serialEvent(SerialPortEvent spe) {
            try{
                if (ardu.isMessageAvailable()){
                    //Se impore el menasje recibido
                    resultado =ardu.printMessage();
                    jLabelDatoRecibido.setText(resultado); //jLabelDatoRecibido ha de ser estatico
                    
                }
            } catch (SerialPortException | ArduinoException ex) {
                Logger.getLogger(JAVARXTX.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    };
