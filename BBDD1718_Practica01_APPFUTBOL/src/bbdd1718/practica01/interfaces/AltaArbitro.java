    private void buttonAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAceptarMouseClicked
        // BotÃ³n para dar de alta al arbitro.
        
        String[] msg = {"Datos incompletos","ID, telefono, salario o email erroneo","Introduce correctamente el email o salario"};
        int msgIndice=1;
        boolean crearArbitro=true; //ture-> Crea estadio, false-> No crea estadio,        
        //Elementos del jugador
        int idArbitro = 0;
        Double salario = 0.0;
        String telefono = "";
        String nombre = "";
        String email = "";
        String posicion = "";//Equivalente a tipo
        
        //
        if(this.textName.getText().equals("") || this.textEmail.getText().equals("") || 
                this.textIdArbitro.getText().equals("") || this.textSalario.getText().equals("") ||
                this.textTelefono.getText().equals("") ){
            //AlgÃºn campo est vacÃ­o.
            msgIndice=0;
            crearArbitro=false;
            
        }else{//Los camos estan completos
            
            try{
                idArbitro = Integer.parseInt(this.textIdArbitro.getText());
                salario = Double.parseDouble(this.textSalario.getText());

            }catch(Exception e){//Introdujo un carracter no numÃ©rico
                msgIndice=2;
                crearArbitro=false;
                
            }
            
            posicion = AppFutbol.posicionesArbitro[ this.selectPosicion.getSelectedIndex() ];
            email = this.textEmail.getText();
            telefono = this.textTelefono.getText();
            nombre = this.textName.getText();
            
            //Comprobamos la estrucutra del email y el telÃ©fono.
            if(!( isEmail() )|| !( isTele() )){//Si el email no tiene la extructura x@x.x y el telefono +XXxxxxxxxx
                
                msgIndice=2;
                crearArbitro = false;
                
            }else{//Si cumple la estructura
                
                 //Comprobamos que los datos sean correctos
                if(idArbitro<0 || salario<=0){//El identificador debe ser mayor o igual que 0.
                    msgIndice=1;
                    crearArbitro = false;
                }else{
                    
                    if(!AppFutbol.lArbitros.isEmpty()){//Lista no vacÃ­a
                        if(AppFutbol.lArbitros.containsKey(idArbitro)){
                            //El id del estadio ya existe en la lista de estadios.
                            int y= JOptionPane.showConfirmDialog(null, "Id del jugador ya en uso, Â¿Modificar estadio de la lista? ",
                                "AppFutbol",JOptionPane.YES_NO_OPTION);
                            if(y==JOptionPane.NO_OPTION){//Dijo que no queria borrar el estadio ya existente.
                                crearArbitro=false;
                                msgIndice=-1;
                            }

                        }//Fin del if, para ver si el identificador esta en uso
                    }//Fin del if, para ver si esta la lista vacia.
                }//Fin del if-else de validar la estrucutra e-amil y telefono
            }//Fin del if-else que comprueba si estan vacios o no los inputs de entrada
        } //Fin del if-esle que compureba si el id y el sarlario
        
        if(!crearArbitro){
            if(msgIndice!=-1){//Si el error no procede del id ya en uso.
                JOptionPane.showMessageDialog(null, msg[msgIndice],
                    "AppFutbol",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            Personas p = new Personas(idArbitro,nombre,email,telefono);
            a = new Arbitro(p,posicion,salario );
            AppFutbol.AltaArbitro(a);

            this.setVisible(false);
            AppFutbol.iu.setVisible(true);
        } 
    }//GEN-LAST:event_buttonAceptarMouseClicked

